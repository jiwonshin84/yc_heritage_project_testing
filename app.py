import streamlit as st
import pandas as pd

st.set_page_config(
    page_title="문화재 위험도 AI 분석",
    layout="wide"
)

# ======================================================
# 제목
# ======================================================
st.title("🏛 공공데이터 기반 문화재 훼손 위험 예측 시스템")

st.markdown("""
문화재 환경 데이터를 기반으로  
AI 군집분석 및 훼손 위험 예측을 수행하는 프로젝트입니다.
""")

# ======================================================
# 데이터 로드
# ======================================================
df = pd.read_csv(
    "data/processed/yc_heritage_detail_enriched.csv"
)

# ======================================================
# 통계 카드
# ======================================================
c1, c2, c3, c4 = st.columns(4)

c1.metric(
    "영천 문화재 수",
    len(df)
)

c2.metric(
    "국보/보물 수",
    len(
        df[
            df["국가유산종목"]
            .isin(["국보","보물"])
        ]
    )
)

c3.metric(
    "시대 종류",
    df["시대"].nunique()
)

c4.metric(
    "문화재 유형",
    df["국가유산종목"].nunique()
)

st.divider()

# ======================================================
# 메뉴 안내
# ======================================================
st.subheader("📚 페이지 안내")

col1, col2 = st.columns(2)

with col1:

    st.page_link(
        "pages/1_프로젝트개요.py",
        label="📖 프로젝트 개요"
    )

    st.page_link(
        "pages/2_전국문화유산현황.py",
        label="🇰🇷 전국 문화유산 현황"
    )

    st.page_link(
        "pages/3_영천문화재공간분석.py",
        label="🗺 영천 문화재 공간분석"
    )

    st.page_link(
        "pages/4_환경데이터분석.py",
        label="🌦 환경 데이터 분석"
    )

with col2:

    st.page_link(
        "pages/5_군집분석.py",
        label="🤖 문화재 군집분석"
    )

    st.page_link(
        "pages/6_훼손위험도예측.py",
        label="⚠ 훼손 위험도 예측"
    )

    st.page_link(
        "pages/7_AI문화재해설.py",
        label="🧠 AI 문화재 해설"
    )

    st.page_link(
        "pages/8_데이터탐색실험실.py",
        label="🧪 데이터 탐색 실험실"
    )

st.divider()

# ======================================================
# 연구 흐름도
# ======================================================
st.subheader("📌 연구 흐름")

st.markdown("""
국가유산 데이터  
+ 기상 데이터  
+ 미세먼지 데이터  
+ 재질/노출형태  
↓  
AI 군집분석  
↓  
문화재 훼손 위험 예측
""")
